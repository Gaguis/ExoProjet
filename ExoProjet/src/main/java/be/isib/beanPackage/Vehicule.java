package be.isib.beanPackage;

/**
 *
 * @author Gagui
 */
public class Vehicule {
    private String nmat, puisfis, caross, marque, type, craburant;
    private int nbr_ass;

    public Vehicule() {
    }

    public String getNmat() {
        return nmat;
    }

    public void setNmat(String nmat) {
        this.nmat = nmat;
    }

    public String getPuisfis() {
        return puisfis;
    }

    public void setPuisfis(String puisfis) {
        this.puisfis = puisfis;
    }

    public String getCaross() {
        return caross;
    }

    public void setCaross(String caross) {
        this.caross = caross;
    }

    public String getMarque() {
        return marque;
    }

    public void setMarque(String marque) {
        this.marque = marque;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCraburant() {
        return craburant;
    }

    public void setCraburant(String craburant) {
        this.craburant = craburant;
    }

    public int getNbr_ass() {
        return nbr_ass;
    }

    public void setNbr_ass(int nbr_ass) {
        this.nbr_ass = nbr_ass;
    }
    
    
}
